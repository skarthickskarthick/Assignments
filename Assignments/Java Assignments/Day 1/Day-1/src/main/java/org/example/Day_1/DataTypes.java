package org.example.Day_1;

public class DataTypes {
    public static void main(String[] args) {
        int varInt = 100;
        double varDouble = 12345.6789;
        float varFloat = 12.34f;
        long varLong = 1234567890123L;
        char varChar = 'A';
        // (-128 to 127)
        byte varByte = 127;
        // (-32,768 to 32,767)
        short varShort = 32000;
        // (true or false)
        boolean varBoolean = true;

        System.out.println("Integer: " + varInt);
        System.out.println("Double: " + varDouble);
        System.out.println("Float: " + varFloat);
        System.out.println("Long: " + varLong);
        System.out.println("Character: " + varChar);
        System.out.println("Byte: " + varByte);
        System.out.println("Short: " + varShort);
        System.out.println("Boolean: " + varBoolean);
    }
}
